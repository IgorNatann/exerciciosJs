// Atribuindo valores a const e let
// Sinal de atribuição é =
const a = 7;
let b = 3;

// Atribuições resumidas/atalhos, fazendo uma operação aritmetica ao mesmo tempo fazendo a atribuição.
// O Operador aritmetico sempre vem antes da atribuição

b += a; // b = b + a
console.log(b);

b -= 4; // b = b - 4 
console.log(b);

b *= 2; // b = b * 2
console.log(b);

b /= 2; // b = b / 2
console.log(b);

b %= 2; // b = b % 2
console.log(b);
