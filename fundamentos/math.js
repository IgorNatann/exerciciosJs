// Math é um objeto que tem uma constante chamaada PI
// Math armazena o valor de PI
const raio = 5.6;
const area = Math.PI * Math.pow(raio,2); // POW = pontencia, passando os elementos dentro dessa função ele faz o calcúlo 

console.log(area);
console.log(area.toFixed(2)); // toFixed é uma função usada para setar quantas casas decimais quero que apareça.
